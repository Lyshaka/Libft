int		ft_atoi(const char *str)
{
	int	i;
	int	ret;
	int	neg;

	ret = 0;
	i = 0;
	neg = 1;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '-')
	{
		neg = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		ret += str[i] - 48;
		if (str[i + 1] >= '0' && str[i + 1] <= '9')
			ret *= 10;
		i++;
	}
	return (ret * neg);
}
